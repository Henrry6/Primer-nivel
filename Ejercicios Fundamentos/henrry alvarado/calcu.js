function sumar (num1,num2){

    let suma;
    suma= parseInt(num1)+ parseInt(num2);

    return suma;
}

function restar (num1,num2){

    let resta;
    resta=num1-num2;

    return resta;
}

function multiplicar (num1,num2){

    let multiplica;
    multiplica=num1*num2;

    return multiplica;
}

function dividir (num1,num2){
    let divide;
    divide=num1/num2;

    return divide;
}
